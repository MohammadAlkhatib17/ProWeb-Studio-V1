// Helper functions for local SEO
export function getCityBySlug(slug: string): DutchCity | undefined {
  return dutchCities.find((city) => city.slug === slug);
}

export function getNearestCities(
  coordinates: { lat: number; lng: number },
  limit: number = 5,
): DutchCity[] {
  return dutchCities
    .map((city) => ({
      ...city,
      distance: calculateDistance(coordinates, city.coordinates),
    }))
    .sort((a, b) => a.distance - b.distance)
    .slice(0, limit);
}

function calculateDistance(
  coord1: { lat: number; lng: number },
  coord2: { lat: number; lng: number },
): number {
  const R = 6371; // Earth's radius in kilometers
  const dLat = ((coord2.lat - coord1.lat) * Math.PI) / 180;
  const dLng = ((coord2.lng - coord1.lng) * Math.PI) / 180;
  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos((coord1.lat * Math.PI) / 180) *
      Math.cos((coord2.lat * Math.PI) / 180) *
      Math.sin(dLng / 2) *
      Math.sin(dLng / 2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  return R * c;
}

export function generateLocalKeywords(
  city: DutchCity,
  baseKeywords: string[],
): string[] {
  const localVariations = [
    ...baseKeywords.map((keyword) => `${keyword} ${city.name.toLowerCase()}`),
    ...baseKeywords.map((keyword) => `${keyword} ${city.slug}`),
    ...baseKeywords.map(
      (keyword) => `${keyword} in ${city.name.toLowerCase()}`,
    ),
    ...baseKeywords.map(
      (keyword) => `${keyword} regio ${city.localInfo.region.toLowerCase()}`,
    ),
    ...baseKeywords.map(
      (keyword) => `${keyword} ${city.province.toLowerCase()}`,
    ),
    ...(city.localInfo.dialCode
      ? baseKeywords.map((keyword) => `${keyword} ${city.localInfo.dialCode}`)
      : []),
  ];

  return Array.from(new Set(localVariations)); // Remove duplicates
}
