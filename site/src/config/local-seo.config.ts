export interface DutchCity {
  name: string;
  slug: string;
  province: string;
  postalCodes: string[];
  coordinates: {
    lat: number;
    lng: number;
  };
  population: number;
  keywords: {
    primary: string[];
    secondary: string[];
    longTail: string[];
  };
  localInfo: {
    chamberOfCommerce?: string;
    municipality: string;
    region: string;
    dialCode: string;
  };
  businessHours?: {
    monday: string;
    tuesday: string;
    wednesday: string;
    thursday: string;
    friday: string;
    saturday?: string;
    sunday?: string;
  };
}

